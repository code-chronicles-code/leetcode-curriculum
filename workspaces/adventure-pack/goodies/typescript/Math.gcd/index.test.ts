import { describe, expect, it } from "@jest/globals";

import "./index";

describe("Math.gcd", () => {
  it.each([
    [48, 18, 6],
    [17, 13, 1],
    [36, 60, 12],
    [5, 0, 5],
    [0, 5, 5],
    [-5, 10, 5],
    [-7, 3, 1],
    [-8, -12, 4],
    [0, 0, 0],
    [1000000007, 1000000009, 1],
    [1000000007, 2, 1],
    [2000000014, 1000000007, 1000000007],
    [1000000007, 7000000049, 1000000007],
    [1000000007, 2, 1],
  ])("Math.gcd(%p, %p) === %p", (a, b, expected) => {
    expect(Math.gcd(a, b)).toBe(expected);
  });
});
